/*Operadores aritmeticos*/

#include <stdio.h>

int main (){
    int a = 10 ,b = 5;
    int suma = a+b;
    int resta = a-b;
    int multi = a*b;
    int division= a/b ;
    int modulo= a%b ;

    printf("Suma = %d\n Resta = %d\n Multiplicacion = %d\n Division = %d\n Modulo(residuo) = %d\n ",suma, resta , multi, division, modulo);


return 0;
}
